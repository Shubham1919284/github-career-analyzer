GENERIC_ROADMAP = {
    "Beginner": {"duration": "90 days"},
    "Internship Seeker": {"duration": "60 days"},
    "Fresher": {"duration": "45 days"},
    "Working Professional": {"duration": "30 days"},
    "Senior": {"duration": "21 days"},
}


def get_roadmap(role_name, experience_level):
    role_roadmap = ROADMAPS.get(role_name, {})
    level_roadmap = role_roadmap.get(experience_level)

    if not level_roadmap:
        return {
            "duration": GENERIC_ROADMAP.get(experience_level, {}).get("duration", "30 days"),
            "weeks": [],
            "message": f"Detailed roadmap for {role_name} coming soon! Check roadmap.sh/{role_name.lower().replace(' ', '-')} for now."
        }

    return level_roadmap


def get_roadmap_summary(role_name, experience_level):
    roadmap = get_roadmap(role_name, experience_level)
    return {
        "role": role_name,
        "level": experience_level,
        "duration": roadmap.get("duration"),
        "total_weeks": len(roadmap.get("weeks", [])),
        "key_topics": [w["title"] for w in roadmap.get("weeks", [])]
    }


